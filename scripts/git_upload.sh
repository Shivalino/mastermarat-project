#!/bin/bash

# Проверяем, передан ли аргумент (сообщение коммита)
if [ -z "$1" ]; then
  echo "Использование: $0 \"Ваше сообщение коммита\""
  exit 1
fi

COMMIT_MESSAGE="$1"

echo "Добавление всех изменений в индекс..."
git add .

echo "Создание коммита с сообщением: \"$COMMIT_MESSAGE\""
git commit -m "$COMMIT_MESSAGE"

echo "Отправка изменений в удаленный репозиторий..."
git push

echo "Операция завершена."

